    var height = appliances_electric[num].dimensions.height;
    var depth = appliances_electric[num].dimensions.depth;
    var images = appliances_electric[num].images;
    var location = "../products/";
    var description = appliances_electric[num].overview;

    localStorage.setItem("locations", location);
    localStorage.setItem("id", num);
    localStorage.setItem("Title", title);
    localStorage.setItem("price", price);
    localStorage.setItem("Description", description);
    localStorage.setItem("category", JSON.stringify(category));
    localStorage.setItem("width", width);
    localStorage.setItem("height", height);
    localStorage.setItem("depth", depth);

